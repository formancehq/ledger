// protoc-gen-dethash generates MarshalDeterministicVT(buf []byte) []byte methods
// for protobuf messages. For messages WITHOUT transitive map fields, it delegates
// to vtprotobuf's MarshalVT (already deterministic). For messages WITH map fields,
// it generates backward-writing code that replicates vtprotobuf's pattern but
// sorts map keys before iterating.
//
// Install:  go build -o protoc-gen-dethash .
// Usage:    protoc --dethash_out=. --dethash_opt=module=<module> ...
package main

import (
	"fmt"

	"google.golang.org/protobuf/compiler/protogen"
	"google.golang.org/protobuf/reflect/protoreflect"
	"google.golang.org/protobuf/types/pluginpb"
)

var (
	slicesPkg       = protogen.GoImportPath("slices")
	syncPkg         = protogen.GoImportPath("sync")
	protohelpersPkg = protogen.GoImportPath("github.com/planetscale/vtprotobuf/protohelpers")
	binaryPkg       = protogen.GoImportPath("encoding/binary")
	mathPkg         = protogen.GoImportPath("math")
)

// keyKindInfo describes how to declare a sync.Pool of sorted keys for a
// supported map-key kind. The generated marshaler grabs a *[]K from the pool,
// fills it, sorts it in place, and returns it to the pool after the map has
// been encoded — replacing the previous `slices.Sorted(maps.Keys(...))` call
// that allocated a fresh slice on every marshal.
type keyKindInfo struct {
	// goType is the Go literal for the key slice element (e.g. "string",
	// "int64").
	goType string
	// pkgVar is the unique package-level variable name for this kind's pool,
	// e.g. "_dethashKeyPoolString".
	pkgVar string
	// sortCall is the sort statement applied to a `keys` slice of this type.
	// Generated as `<package>.<call>(keys)` so the import is tracked.
	sortPkg  protogen.GoImportPath
	sortFunc string
}

// supportedKeyKinds maps every map-key kind the plugin accepts to its
// pool/sort recipe. The kinds match the panic guard in genMapMarshal.
// `slices.Sort` is used uniformly (Go 1.21+ pdqsort generic) — measured
// marginally faster than `sort.Strings` on strings of any size and the
// fastest std-lib option on ints; using a single sort function avoids
// pulling the `sort` package into the generated file.
var supportedKeyKinds = map[protoreflect.Kind]keyKindInfo{
	protoreflect.StringKind: {goType: "string", pkgVar: "_dethashKeyPoolString", sortPkg: slicesPkg, sortFunc: "Sort"},
	protoreflect.Int32Kind:  {goType: "int32", pkgVar: "_dethashKeyPoolInt32", sortPkg: slicesPkg, sortFunc: "Sort"},
	protoreflect.Uint32Kind: {goType: "uint32", pkgVar: "_dethashKeyPoolUint32", sortPkg: slicesPkg, sortFunc: "Sort"},
	protoreflect.Int64Kind:  {goType: "int64", pkgVar: "_dethashKeyPoolInt64", sortPkg: slicesPkg, sortFunc: "Sort"},
	protoreflect.Uint64Kind: {goType: "uint64", pkgVar: "_dethashKeyPoolUint64", sortPkg: slicesPkg, sortFunc: "Sort"},
}

func main() {
	protogen.Options{}.Run(func(gen *protogen.Plugin) error {
		gen.SupportedFeatures = uint64(pluginpb.CodeGeneratorResponse_FEATURE_PROTO3_OPTIONAL)

		// First pass: compute which messages transitively contain maps.
		hasMapTransitive := make(map[string]bool)
		for _, f := range gen.Files {
			for _, msg := range f.Messages {
				computeHasMap(msg, hasMapTransitive)
			}
		}

		for _, f := range gen.Files {
			if !f.Generate {
				continue
			}
			generateFile(gen, f, hasMapTransitive)
		}
		return nil
	})
}

func computeHasMap(msg *protogen.Message, cache map[string]bool) bool {
	key := string(msg.Desc.FullName())
	if v, ok := cache[key]; ok {
		return v
	}
	cache[key] = false // prevent cycles

	for _, f := range msg.Fields {
		if f.Desc.IsMap() {
			cache[key] = true
			return true
		}
		if f.Desc.Kind() == protoreflect.MessageKind && f.Message != nil && !f.Message.Desc.IsMapEntry() {
			if computeHasMap(f.Message, cache) {
				cache[key] = true
				return true
			}
		}
	}
	return cache[key]
}

func generateFile(gen *protogen.Plugin, file *protogen.File, hasMap map[string]bool) {
	if len(file.Messages) == 0 {
		return
	}
	g := gen.NewGeneratedFile(
		file.GeneratedFilenamePrefix+"_dethash.pb.go",
		file.GoImportPath,
	)
	g.P("// Code generated by protoc-gen-dethash. DO NOT EDIT.")
	g.P("// source: ", file.Desc.Path())
	g.P()
	g.P("package ", file.GoPackageName)

	// First, find every map-key kind used in this file so we can emit one
	// sync.Pool per kind. The pool reuses the sorted-keys slice across
	// marshal calls, replacing the per-call `slices.Sorted(maps.Keys(m))`
	// allocation. The pool is package-private so it does not leak into
	// the public API.
	usedKeyKinds := map[protoreflect.Kind]bool{}
	for _, msg := range file.Messages {
		collectMapKeyKinds(msg, usedKeyKinds)
	}

	emitKeyPools(g, usedKeyKinds)

	for _, msg := range file.Messages {
		walkMessages(g, msg, hasMap)
	}
}

// collectMapKeyKinds walks msg + every nested message and records the kind
// of every map<K, V> field's key. Limited to the kinds supported by
// genMapMarshal — anything else would have panicked the plugin elsewhere.
func collectMapKeyKinds(msg *protogen.Message, out map[protoreflect.Kind]bool) {
	if msg.Desc.IsMapEntry() {
		return
	}

	for _, f := range msg.Fields {
		if f.Desc.IsMap() {
			keyKind := f.Message.Fields[0].Desc.Kind()
			if _, ok := supportedKeyKinds[keyKind]; ok {
				out[keyKind] = true
			}
		}
	}

	for _, nested := range msg.Messages {
		collectMapKeyKinds(nested, out)
	}
}

// emitKeyPools writes one sync.Pool per used map-key kind, plus a small
// initial slice capacity tuned for the typical metadata-style map (~16
// entries — beyond that the pool's slice grows once and stays grown,
// which is exactly the behaviour we want).
func emitKeyPools(g *protogen.GeneratedFile, used map[protoreflect.Kind]bool) {
	if len(used) == 0 {
		return
	}

	// Iterate in a deterministic order so the generated file is stable
	// across plugin runs (map iteration would otherwise reshuffle the
	// declarations, producing useless diffs).
	ordered := []protoreflect.Kind{
		protoreflect.StringKind,
		protoreflect.Int32Kind,
		protoreflect.Uint32Kind,
		protoreflect.Int64Kind,
		protoreflect.Uint64Kind,
	}

	g.P()
	g.P("// Map-key scratch pools. Each MarshalToSizedBufferDeterministicVT that")
	g.P("// encodes a map<K, V> grabs a *[]K from the matching pool, fills it,")
	g.P("// sorts it in place, and returns it. Steady-state allocations are zero:")
	g.P("// the pool reuses the slice across marshal calls. The first marshal of")
	g.P("// a given kind warms the pool with a 16-cap slice; larger maps grow it")
	g.P("// once and the grown capacity persists.")
	g.P("var (")

	for _, kind := range ordered {
		if !used[kind] {
			continue
		}

		info := supportedKeyKinds[kind]
		g.P("  ", info.pkgVar, " = ", syncPkg.Ident("Pool"), "{")
		g.P("    New: func() any { s := make([]", info.goType, ", 0, 16); return &s },")
		g.P("  }")
	}

	g.P(")")
}

func walkMessages(g *protogen.GeneratedFile, msg *protogen.Message, hasMap map[string]bool) {
	if msg.Desc.IsMapEntry() {
		return
	}
	generateMessage(g, msg, hasMap)
	for _, nested := range msg.Messages {
		walkMessages(g, nested, hasMap)
	}
}

func generateMessage(g *protogen.GeneratedFile, msg *protogen.Message, hasMap map[string]bool) {
	key := string(msg.Desc.FullName())

	g.P()
	if !hasMap[key] {
		// No transitive maps — delegate to vtprotobuf (already deterministic).
		g.P("func (m *", msg.GoIdent, ") MarshalDeterministicVT(dAtA []byte) []byte {")
		g.P("  if m == nil { return dAtA }")
		g.P("  b, err := m.MarshalVT()")
		g.P("  if err != nil { panic(\"MarshalDeterministicVT: \" + err.Error()) }")
		g.P("  return append(dAtA, b...)")
		g.P("}")
	} else {
		// Has transitive maps — generate deterministic backward-writing.
		g.P("func (m *", msg.GoIdent, ") MarshalDeterministicVT(dAtA []byte) []byte {")
		g.P("  if m == nil { return dAtA }")
		g.P("  sz := m.SizeVT()")
		g.P("  buf := make([]byte, sz)")
		g.P("  n, _ := m.MarshalToSizedBufferDeterministicVT(buf)")
		g.P("  return append(dAtA, buf[sz-n:]...)")
		g.P("}")
		g.P()
		g.P("func (m *", msg.GoIdent, ") MarshalToSizedBufferDeterministicVT(dAtA []byte) (int, error) {")
		g.P("  if m == nil { return 0, nil }")
		g.P("  i := len(dAtA)")

		// unknownFields (same as vtprotobuf)
		g.P("  if m.unknownFields != nil {")
		g.P("    i -= len(m.unknownFields)")
		g.P("    copy(dAtA[i:], m.unknownFields)")
		g.P("  }")

		// Fields in reverse order (backward writing)
		for idx := len(msg.Fields) - 1; idx >= 0; idx-- {
			f := msg.Fields[idx]
			if f.Oneof != nil && !f.Oneof.Desc.IsSynthetic() {
				continue
			}
			genFieldMarshal(g, f, hasMap)
		}

		// Oneofs in reverse
		seenOneofs := map[string]bool{}
		for idx := len(msg.Fields) - 1; idx >= 0; idx-- {
			f := msg.Fields[idx]
			if f.Oneof == nil || f.Oneof.Desc.IsSynthetic() {
				continue
			}
			if seenOneofs[f.Oneof.GoName] {
				continue
			}
			seenOneofs[f.Oneof.GoName] = true
			genOneofMarshal(g, msg, f.Oneof, hasMap)
		}

		g.P("  return len(dAtA) - i, nil")
		g.P("}")
	}
}

// ---------- oneof ----------

func genOneofMarshal(g *protogen.GeneratedFile, msg *protogen.Message, oo *protogen.Oneof, hasMap map[string]bool) {
	// Check if ANY variant transitively contains maps.
	anyHasMap := false
	for _, f := range oo.Fields {
		if f.Desc.Kind() == protoreflect.MessageKind && f.Message != nil {
			if hasMap[string(f.Message.Desc.FullName())] {
				anyHasMap = true
				break
			}
		}
	}

	if !anyHasMap {
		// No maps in any variant — delegate to vtprotobuf (already deterministic).
		g.P("  if vtmsg, ok := m.", oo.GoName, ".(interface{ MarshalToSizedBufferVT([]byte) (int, error) }); ok {")
		g.P("    size, _ := vtmsg.MarshalToSizedBufferVT(dAtA[:i])")
		g.P("    i -= size")
		g.P("  }")
	} else {
		// Some variants have maps — must handle deterministically per variant.
		g.P("  switch v := m.", oo.GoName, ".(type) {")
		for _, f := range oo.Fields {
			variant := fmt.Sprintf("*%s_%s", msg.GoIdent.GoName, f.GoName)
			g.P("  case ", variant, ":")
			marshalOneofField(g, f, hasMap, "v")
		}
		g.P("  }")
	}
}

func marshalOneofField(g *protogen.GeneratedFile, f *protogen.Field, hasMap map[string]bool, recv string) {
	access := fmt.Sprintf("%s.%s", recv, f.GoName)
	kind := f.Desc.Kind()
	fieldNum := f.Desc.Number()

	if kind == protoreflect.MessageKind || kind == protoreflect.GroupKind {
		key := string(f.Message.Desc.FullName())
		if hasMap[key] {
			// Sub-message has maps — use deterministic marshal, with nil guard.
			g.P("    if ", access, " != nil {")
			g.P("      size, _ := ", access, ".MarshalToSizedBufferDeterministicVT(dAtA[:i])")
			g.P("      i -= size")
			g.P("      i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(size))")
			writeTag(g, encodeTag(fieldNum, 2))
			g.P("    }")
		} else {
			// No maps — delegate to vtprotobuf, with nil guard.
			g.P("    if ", access, " != nil {")
			g.P("      size, _ := ", access, ".MarshalToSizedBufferVT(dAtA[:i])")
			g.P("      i -= size")
			g.P("      i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(size))")
			writeTag(g, encodeTag(fieldNum, 2))
			g.P("    }")
		}
	} else {
		wireType := protoWireType(kind)
		marshalScalarField(g, access, kind, fieldNum, wireType)
	}
}

// ---------- field ----------

func genFieldMarshal(g *protogen.GeneratedFile, f *protogen.Field, hasMap map[string]bool) {
	goName := f.GoName
	kind := f.Desc.Kind()
	fieldNum := f.Desc.Number()
	access := "m." + goName

	if f.Desc.IsMap() {
		genMapMarshal(g, f, hasMap)
		return
	}

	if f.Desc.IsList() {
		genRepeatedMarshal(g, f, hasMap)
		return
	}

	if kind == protoreflect.MessageKind || kind == protoreflect.GroupKind {
		g.P("  if ", access, " != nil {")
		marshalMessageField(g, access, f, fieldNum, hasMap)
		g.P("  }")
		return
	}

	wireType := protoWireType(kind)

	if isProto3Optional(f) {
		g.P("  if ", access, " != nil {")
		marshalScalarField(g, "*"+access, kind, fieldNum, wireType)
		g.P("  }")
		return
	}

	g.P("  if ", zeroGuard(access, kind), " {")
	marshalScalarField(g, access, kind, fieldNum, wireType)
	g.P("  }")
}

// ---------- map ----------

func genMapMarshal(g *protogen.GeneratedFile, f *protogen.Field, hasMap map[string]bool) {
	goName := f.GoName
	valField := f.Message.Fields[1]
	fieldNum := f.Desc.Number()
	tag := encodeTag(fieldNum, 2)
	keyField := f.Message.Fields[0]
	keyKind := keyField.Desc.Kind()

	info, ok := supportedKeyKinds[keyKind]
	if !ok {
		panic(fmt.Sprintf("protoc-gen-dethash: unsupported map key type %s in field %s", keyKind, f.Desc.FullName()))
	}

	// Borrow a sorted-keys scratch slice from the file-scoped pool, fill it,
	// sort it in place, and return it after the loop. Steady-state zero alloc.
	g.P("  if len(m.", goName, ") > 0 {")
	g.P("    keysPtr := ", info.pkgVar, ".Get().(*[]", info.goType, ")")
	g.P("    keys := (*keysPtr)[:0]")
	g.P("    for k := range m.", goName, " {")
	g.P("      keys = append(keys, k)")
	g.P("    }")
	g.P("    ", info.sortPkg.Ident(info.sortFunc), "(keys)")
	g.P("    for _, k := range keys {")
	g.P("      v := m.", goName, "[k]")
	g.P("      baseI := i")

	// Write value (field 2 of map entry)
	valKind := valField.Desc.Kind()
	if valKind == protoreflect.MessageKind {
		key := string(valField.Message.Desc.FullName())
		if hasMap[key] {
			g.P("      size, _ := v.MarshalToSizedBufferDeterministicVT(dAtA[:i])")
		} else {
			g.P("      size, _ := v.MarshalToSizedBufferVT(dAtA[:i])")
		}
		g.P("      i -= size")
		g.P("      i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(size))")
		g.P("      i--")
		g.P("      dAtA[i] = 0x12")
	} else if valKind == protoreflect.StringKind {
		g.P("      i -= len(v)")
		g.P("      copy(dAtA[i:], v)")
		g.P("      i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(len(v)))")
		g.P("      i--")
		g.P("      dAtA[i] = 0x12")
	} else if valKind == protoreflect.EnumKind || valKind == protoreflect.Int32Kind || valKind == protoreflect.Uint32Kind || valKind == protoreflect.Int64Kind || valKind == protoreflect.Uint64Kind {
		g.P("      i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(v))")
		g.P("      i--")
		g.P("      dAtA[i] = 0x10") // field 2, wire type varint
	} else {
		panic(fmt.Sprintf("protoc-gen-dethash: unsupported map value type %s in field %s", valKind, f.Desc.FullName()))
	}

	// Write key (field 1)
	switch keyKind {
	case protoreflect.StringKind:
		g.P("      i -= len(k)")
		g.P("      copy(dAtA[i:], k)")
		g.P("      i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(len(k)))")
		g.P("      i--")
		g.P("      dAtA[i] = 0xa") // field 1, wire type LEN
	case protoreflect.Uint64Kind, protoreflect.Int64Kind, protoreflect.Uint32Kind, protoreflect.Int32Kind:
		g.P("      i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(k))")
		g.P("      i--")
		g.P("      dAtA[i] = 0x8") // field 1, wire type varint
	default:
		panic(fmt.Sprintf("protoc-gen-dethash: unsupported map key type %s in field %s", keyKind, f.Desc.FullName()))
	}

	// Write entry length + outer tag
	g.P("      i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(baseI-i))")
	writeTag(g, tag)

	g.P("    }")
	// Return the (possibly grown) scratch slice to the pool. The pool keeps
	// the underlying capacity, so the next marshal that needs at most that
	// many keys hits zero allocation.
	g.P("    *keysPtr = keys")
	g.P("    ", info.pkgVar, ".Put(keysPtr)")
	g.P("  }")
}

// ---------- repeated ----------

func genRepeatedMarshal(g *protogen.GeneratedFile, f *protogen.Field, hasMap map[string]bool) {
	goName := f.GoName
	kind := f.Desc.Kind()
	fieldNum := f.Desc.Number()

	g.P("  if len(m.", goName, ") > 0 {")

	if kind == protoreflect.MessageKind || kind == protoreflect.GroupKind {
		g.P("    for iNdEx := len(m.", goName, ") - 1; iNdEx >= 0; iNdEx-- {")
		marshalMessageField(g, "m."+goName+"[iNdEx]", f, fieldNum, hasMap)
		g.P("    }")
	} else if kind == protoreflect.StringKind {
		tag := encodeTag(fieldNum, 2)
		g.P("    for iNdEx := len(m.", goName, ") - 1; iNdEx >= 0; iNdEx-- {")
		g.P("      i -= len(m.", goName, "[iNdEx])")
		g.P("      copy(dAtA[i:], m.", goName, "[iNdEx])")
		g.P("      i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(len(m.", goName, "[iNdEx])))")
		writeTag(g, tag)
		g.P("    }")
	} else if kind == protoreflect.BytesKind {
		tag := encodeTag(fieldNum, 2)
		g.P("    for iNdEx := len(m.", goName, ") - 1; iNdEx >= 0; iNdEx-- {")
		g.P("      i -= len(m.", goName, "[iNdEx])")
		g.P("      copy(dAtA[i:], m.", goName, "[iNdEx])")
		g.P("      i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(len(m.", goName, "[iNdEx])))")
		writeTag(g, tag)
		g.P("    }")
	} else if kind == protoreflect.Uint64Kind || kind == protoreflect.Int64Kind || kind == protoreflect.Uint32Kind || kind == protoreflect.Int32Kind || kind == protoreflect.EnumKind {
		// Packed encoding for varint types
		tag := encodeTag(fieldNum, 2) // packed = wire type LEN
		g.P("    var pkBuf [10]byte")
		g.P("    start := i")
		g.P("    for iNdEx := len(m.", goName, ") - 1; iNdEx >= 0; iNdEx-- {")
		g.P("      n := ", protohelpersPkg.Ident("EncodeVarint"), "(pkBuf[:], 10, uint64(m.", goName, "[iNdEx]))")
		g.P("      i -= 10 - n")
		g.P("      copy(dAtA[i:], pkBuf[n:])")
		g.P("    }")
		g.P("    i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(start-i))")
		writeTag(g, tag)
	} else if kind == protoreflect.Fixed64Kind || kind == protoreflect.Sfixed64Kind || kind == protoreflect.DoubleKind {
		tag := encodeTag(fieldNum, 2)
		g.P("    i -= 8 * len(m.", goName, ")")
		g.P("    for iNdEx, v := range m.", goName, " {")
		if kind == protoreflect.DoubleKind {
			g.P("      ", binaryPkg.Ident("LittleEndian"), ".PutUint64(dAtA[i+iNdEx*8:], ", mathPkg.Ident("Float64bits"), "(v))")
		} else {
			g.P("      ", binaryPkg.Ident("LittleEndian"), ".PutUint64(dAtA[i+iNdEx*8:], uint64(v))")
		}
		g.P("    }")
		g.P("    i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(8*len(m.", goName, ")))")
		writeTag(g, tag)
	} else if kind == protoreflect.Fixed32Kind || kind == protoreflect.Sfixed32Kind || kind == protoreflect.FloatKind {
		tag := encodeTag(fieldNum, 2)
		g.P("    i -= 4 * len(m.", goName, ")")
		g.P("    for iNdEx, v := range m.", goName, " {")
		if kind == protoreflect.FloatKind {
			g.P("      ", binaryPkg.Ident("LittleEndian"), ".PutUint32(dAtA[i+iNdEx*4:], ", mathPkg.Ident("Float32bits"), "(v))")
		} else {
			g.P("      ", binaryPkg.Ident("LittleEndian"), ".PutUint32(dAtA[i+iNdEx*4:], uint32(v))")
		}
		g.P("    }")
		g.P("    i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(4*len(m.", goName, ")))")
		writeTag(g, tag)
	} else if kind == protoreflect.BoolKind {
		tag := encodeTag(fieldNum, 2)
		g.P("    i -= len(m.", goName, ")")
		g.P("    for iNdEx, v := range m.", goName, " {")
		g.P("      if v { dAtA[i+iNdEx] = 1 } else { dAtA[i+iNdEx] = 0 }")
		g.P("    }")
		g.P("    i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(len(m.", goName, ")))")
		writeTag(g, tag)
	}

	g.P("  }")
}

// ---------- message marshal ----------

func marshalMessageField(g *protogen.GeneratedFile, access string, f *protogen.Field, fieldNum protoreflect.FieldNumber, hasMap map[string]bool) {
	tag := encodeTag(fieldNum, 2)
	key := string(f.Message.Desc.FullName())

	if hasMap[key] {
		g.P("    size, _ := ", access, ".MarshalToSizedBufferDeterministicVT(dAtA[:i])")
		g.P("    i -= size")
	} else {
		g.P("    size, _ := ", access, ".MarshalToSizedBufferVT(dAtA[:i])")
		g.P("    i -= size")
	}
	g.P("    i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(size))")
	writeTag(g, tag)
}

// ---------- scalar marshal ----------

func marshalScalarField(g *protogen.GeneratedFile, access string, kind protoreflect.Kind, fieldNum protoreflect.FieldNumber, wireType int) {
	tag := encodeTag(fieldNum, wireType)

	switch kind {
	case protoreflect.BoolKind:
		g.P("    i--")
		g.P("    if ", access, " { dAtA[i] = 1 } else { dAtA[i] = 0 }")
		writeTag(g, tag)

	case protoreflect.Int32Kind, protoreflect.Int64Kind,
		protoreflect.Uint32Kind, protoreflect.Uint64Kind,
		protoreflect.EnumKind:
		g.P("    i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(", access, "))")
		writeTag(g, tag)

	case protoreflect.Sint32Kind:
		g.P("    i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(uint32(", access, ")<<1^uint32(int32(", access, ")>>31)))")
		writeTag(g, tag)

	case protoreflect.Sint64Kind:
		g.P("    i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(uint64(", access, ")<<1^uint64(int64(", access, ")>>63)))")
		writeTag(g, tag)

	case protoreflect.Fixed32Kind, protoreflect.Sfixed32Kind:
		g.P("    i -= 4")
		g.P("    ", binaryPkg.Ident("LittleEndian"), ".PutUint32(dAtA[i:], uint32(", access, "))")
		writeTag(g, tag)

	case protoreflect.Fixed64Kind, protoreflect.Sfixed64Kind:
		g.P("    i -= 8")
		g.P("    ", binaryPkg.Ident("LittleEndian"), ".PutUint64(dAtA[i:], uint64(", access, "))")
		writeTag(g, tag)

	case protoreflect.FloatKind:
		g.P("    i -= 4")
		g.P("    ", binaryPkg.Ident("LittleEndian"), ".PutUint32(dAtA[i:], ", mathPkg.Ident("Float32bits"), "(", access, "))")
		writeTag(g, tag)

	case protoreflect.DoubleKind:
		g.P("    i -= 8")
		g.P("    ", binaryPkg.Ident("LittleEndian"), ".PutUint64(dAtA[i:], ", mathPkg.Ident("Float64bits"), "(", access, "))")
		writeTag(g, tag)

	case protoreflect.StringKind:
		g.P("    i -= len(", access, ")")
		g.P("    copy(dAtA[i:], ", access, ")")
		g.P("    i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(len(", access, ")))")
		writeTag(g, tag)

	case protoreflect.BytesKind:
		g.P("    i -= len(", access, ")")
		g.P("    copy(dAtA[i:], ", access, ")")
		g.P("    i = ", protohelpersPkg.Ident("EncodeVarint"), "(dAtA, i, uint64(len(", access, ")))")
		writeTag(g, tag)
	}
}

// ---------- helpers ----------

func isProto3Optional(f *protogen.Field) bool {
	return f.Desc.HasOptionalKeyword()
}

func zeroGuard(access string, kind protoreflect.Kind) string {
	switch kind {
	case protoreflect.BoolKind:
		return access
	case protoreflect.StringKind, protoreflect.BytesKind:
		return fmt.Sprintf("len(%s) > 0", access)
	default:
		return fmt.Sprintf("%s != 0", access)
	}
}

func protoWireType(kind protoreflect.Kind) int {
	switch kind {
	case protoreflect.BoolKind, protoreflect.Int32Kind, protoreflect.Int64Kind,
		protoreflect.Uint32Kind, protoreflect.Uint64Kind,
		protoreflect.Sint32Kind, protoreflect.Sint64Kind, protoreflect.EnumKind:
		return 0
	case protoreflect.Fixed64Kind, protoreflect.Sfixed64Kind, protoreflect.DoubleKind:
		return 1
	case protoreflect.StringKind, protoreflect.BytesKind, protoreflect.MessageKind, protoreflect.GroupKind:
		return 2
	case protoreflect.Fixed32Kind, protoreflect.Sfixed32Kind, protoreflect.FloatKind:
		return 5
	default:
		return 0
	}
}

func encodeTag(fieldNum protoreflect.FieldNumber, wireType int) uint64 {
	return uint64(fieldNum)<<3 | uint64(wireType)
}

func writeTag(g *protogen.GeneratedFile, tag uint64) {
	if tag < 0x80 {
		g.P("    i--")
		g.P(fmt.Sprintf("    dAtA[i] = 0x%x", tag))
	} else if tag < 0x4000 {
		g.P("    i--")
		g.P(fmt.Sprintf("    dAtA[i] = 0x%x", tag>>7))
		g.P("    i--")
		g.P(fmt.Sprintf("    dAtA[i] = 0x%x", tag&0x7f|0x80))
	}
}

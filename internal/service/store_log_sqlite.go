// SQLiteLogStore is a SQLite implementation of LogStore
// Can be used with either github.com/mattn/go-sqlite3 or modernc.org/sqlite driver
type SQLiteLogStore struct {
	db     *SQLDB
	logger logging.Logger

	// Prepared statements
	stmtGetLogByID             *sql.Stmt
	stmtGetLogByIdempotencyKey *sql.Stmt
	stmtGetLastLog             *sql.Stmt
	stmtInsertLog              *sql.Stmt
}

// NewSQLiteLogStore creates a new SQLiteLogStore instance
func NewSQLiteLogStore(db *SQLDB, logger logging.Logger) (*SQLiteLogStore, error) {
	store := &SQLiteLogStore{
		db:     db,
		logger: logger,
	}

	// Create tables first (required before preparing statements)
	ctx := context.Background()
	if err := store.createTables(ctx); err != nil {
		return nil, fmt.Errorf("creating tables: %w", err)
	}

	// Prepare all statements
	var err error
	store.stmtGetLogByID, err = db.PrepareContext(ctx, `
		SELECT id, data, date, idempotency_key, idempotency_hash
		FROM logs
		WHERE id = ?
	`)
	if err != nil {
		return nil, fmt.Errorf("preparing getLogByID statement: %w", err)
	}

	store.stmtGetLogByIdempotencyKey, err = db.PrepareContext(ctx, `
		SELECT id, data, date, idempotency_key, idempotency_hash
		FROM logs
		WHERE idempotency_key = ?
	`)
	if err != nil {
		return nil, fmt.Errorf("preparing getLogByIdempotencyKey statement: %w", err)
	}

	store.stmtGetLastLog, err = db.PrepareContext(ctx, `
		SELECT id, data, date, idempotency_key, idempotency_hash
		FROM logs
		ORDER BY id DESC
		LIMIT 1
	`)
	if err != nil {
		return nil, fmt.Errorf("preparing getLastLog statement: %w", err)
	}

	store.stmtInsertLog, err = db.PrepareContext(ctx, `
		INSERT INTO logs (data, date, idempotency_key, idempotency_hash, id)
		VALUES (?, ?, ?, ?, ?)
	`)
	if err != nil {
		return nil, fmt.Errorf("preparing insertLog statement: %w", err)
	}

	return store, nil
}

// sqliteLogCursor implements Cursor[*ledgerpb.Log] for SQLite
type sqliteLogCursor struct {
	rows  *sql.Rows
	store *SQLiteLogStore
}


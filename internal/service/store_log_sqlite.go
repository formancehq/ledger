// scanLog scans a row into a protobuf Log struct
func (s *SQLiteLogStore) scanLog(row *sql.Row) (*ledgerpb.Log, error) {
	var id sql.NullInt64
	var dataBinary []byte
	var dateStr sql.NullString
	var idempotencyKey sql.NullString
	var idempotencyHash sql.Null[[]byte]

	err := row.Scan(&id, &dataBinary, &dateStr, &idempotencyKey, &idempotencyHash)
	if err != nil {
		return nil, err
	}

	log := &ledgerpb.Log{}

	// Set ID
	if id.Valid {
		log.Id = uint64(id.Int64)
	}

	// Unmarshal protobuf LogPayload from binary
	logPayload := &ledgerpb.LogPayload{}
	if err := proto.Unmarshal(dataBinary, logPayload); err != nil {
		return nil, fmt.Errorf("unmarshaling log payload from protobuf: %w", err)
	}
	log.Data = logPayload

	// Set date
	if dateStr.Valid {
		date, err := stdtime.Parse(stdtime.RFC3339, dateStr.String)
		if err != nil {
			return nil, fmt.Errorf("parsing date: %w", err)
		}
		log.Date = ledgerpb.NewTimestamp(time.New(date))
	}

	// Set idempotency fields
	if idempotencyKey.Valid {
		log.Idempotency = &ledgerpb.Idempotency{
			Key:  idempotencyKey.String,
			Hash: idempotencyHash.V,
		}
	}

	return log, nil
}

func (c *sqliteLogCursor) Next(ctx context.Context) (*ledgerpb.Log, error) {
	if !c.rows.Next() {
		if err := c.rows.Err(); err != nil {
			return nil, err
		}
		return nil, io.EOF
	}

	var (
		id              sql.NullInt64
		dataBinary      []byte
		dateStr         sql.NullString
		idempotencyKey  sql.NullString
		idempotencyHash sql.Null[[]byte]
	)

	err := c.rows.Scan(&id, &dataBinary, &dateStr, &idempotencyKey, &idempotencyHash)
	if err != nil {
		return nil, fmt.Errorf("scanning log row: %w", err)
	}

	log := &ledgerpb.Log{}

	// Set ID
	if id.Valid {
		log.Id = uint64(id.Int64)
	}

	// Unmarshal protobuf LogPayload from binary
	logPayload := &ledgerpb.LogPayload{}
	if err := proto.Unmarshal(dataBinary, logPayload); err != nil {
		return nil, fmt.Errorf("unmarshaling log payload from protobuf: %w", err)
	}
	log.Data = logPayload

	// Set date
	if dateStr.Valid {
		date, err := stdtime.Parse(stdtime.RFC3339, dateStr.String)
		if err != nil {
			return nil, fmt.Errorf("parsing date: %w", err)
		}
		log.Date = ledgerpb.NewTimestamp(time.New(date))
	}

	// Set idempotency fields
	if idempotencyKey.Valid {
		log.Idempotency = &ledgerpb.Idempotency{
			Key:  idempotencyKey.String,
			Hash: idempotencyHash.V,
		}
	}

	return log, nil
}

func (c *sqliteLogCursor) Close() error {
	if c.rows != nil {
		return c.rows.Close()
	}
	return nil
}

// GetAllLogs returns a cursor to iterate over all logs (implements LogReader)
// Logs are returned in ascending order by id
// from: optional log id to start from (0 = from beginning)
func (s *SQLiteLogStore) GetAllLogs(ctx context.Context, from uint64, to uint64) (Cursor[*ledgerpb.Log], error) {
	query := `
		SELECT id, data, date, idempotency_key, idempotency_hash
		FROM logs
	`
	args := []interface{}{}
	whereClauses := []string{}
	if from > 0 {
		whereClauses = append(whereClauses, `id > ?`)
		args = append(args, int64(from))
	}
	if to > 0 {
		whereClauses = append(whereClauses, `id <= ?`)
		args = append(args, int64(to))
	}
	if len(whereClauses) > 0 {
		query += ` WHERE ` + whereClauses[0]
		for i := 1; i < len(whereClauses); i++ {
			query += ` AND ` + whereClauses[i]
		}
	}
	query += ` ORDER BY id ASC`

	rows, err := s.db.QueryContext(ctx, query, args...)
	if err != nil {
		return nil, fmt.Errorf("querying logs: %w", err)
	}

	return &sqliteLogCursor{
		rows:  rows,
		store: s,
	}, nil
}

// GetLogByID retrieves a log by its ID (implements LogReader)
func (s *SQLiteLogStore) GetLogByID(ctx context.Context, id uint64) (*ledgerpb.Log, error) {
	return s.GetLogWithID(ctx, id)
}

// GetLogWithID retrieves a log by its ID
func (s *SQLiteLogStore) GetLogWithID(ctx context.Context, id uint64) (*ledgerpb.Log, error) {
	row := s.stmtGetLogByID.QueryRowContext(ctx, id)

	log, err := s.scanLog(row)
	if err != nil {
		if err == sql.ErrNoRows {
			return nil, nil
		}
		return nil, fmt.Errorf("getting log by id: %w", err)
	}
	return log, nil
}

// GetLogWithIdempotencyKey retrieves a log by its idempotency key (implements LogReader)
func (s *SQLiteLogStore) GetLogWithIdempotencyKey(ctx context.Context, idempotencyKey string) (*ledgerpb.Log, error) {
	row := s.stmtGetLogByIdempotencyKey.QueryRowContext(ctx, idempotencyKey)

	log, err := s.scanLog(row)
	if err != nil {
		if err == sql.ErrNoRows {
			return nil, nil
		}
		return nil, fmt.Errorf("getting log by idempotency key: %w", err)
	}
	return log, nil
}

// GetLastLog retrieves the last log by ID (implements LogReader)
func (s *SQLiteLogStore) GetLastLog(ctx context.Context) (*ledgerpb.Log, error) {
	row := s.stmtGetLastLog.QueryRowContext(ctx)

	log, err := s.scanLog(row)
	if err != nil {
		if err == sql.ErrNoRows {
			return nil, nil
		}
		return nil, fmt.Errorf("getting last log: %w", err)
	}
	return log, nil
}


// applyBalanceDiffs applies accumulated balance differences to the database
// Uses INSERT ... ON CONFLICT DO UPDATE to add the diff directly in SQL
func (s *SQLiteModernLogStore) applyBalanceDiffs(ctx context.Context, tx *sql.Tx, balanceDiffs map[string]map[string]*big.Int) error {
	if len(balanceDiffs) == 0 {
		return nil
	}

	// Prepare statement with ON CONFLICT DO UPDATE to add the diff directly
	// When balance doesn't exist: INSERT creates it with the diff value
	// When balance exists: ON CONFLICT adds the diff to existing balance
	stmt, err := tx.PrepareContext(ctx, `
		INSERT INTO balances (account, asset, balance)
		VALUES (?, ?, ?)
		ON CONFLICT (account, asset) DO UPDATE
		SET balance = bigint_add(balances.balance, excluded.balance)
	`)
	if err != nil {
		return fmt.Errorf("preparing balance update statement: %w", err)
	}
	defer stmt.Close()

	// Apply all diffs - SQL will handle adding to existing balance or inserting new
	for account, assets := range balanceDiffs {
		for asset, diff := range assets {
			// Insert the diff value - SQL will add it to existing balance or use it as initial value
			_, err = stmt.ExecContext(ctx, account, asset, diff.String())
			if err != nil {
				return fmt.Errorf("updating balance for account %s asset %s: %w", account, asset, err)
			}
		}
	}

	return nil
}


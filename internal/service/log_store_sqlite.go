// SQLiteConfig represents the configuration for a SQLite bucket driver
type SQLiteConfig struct {
	DSN string `json:"dsn"` // Data Source Name (connection string)
}

// SQLiteLogStore is a SQLite implementation of LogStore
type SQLiteLogStore struct {
	db     *sql.DB
	logger logging.Logger
}

// NewSQLiteLogStore creates a new SQLite log store
func NewSQLiteLogStore(ctx context.Context, dsn string, logger logging.Logger) (*SQLiteLogStore, error) {

	// Open SQLite database
	db, err := sql.Open("sqlite", dsn)
	if err != nil {
		return nil, fmt.Errorf("opening sqlite database: %w", err)
	}

	store := &SQLiteLogStore{
		db:     db,
		logger: logger,
	}

	// Create tables if they don't exist
	if err := store.createTables(ctx); err != nil {
		return nil, fmt.Errorf("creating tables: %w", err)
	}

	return store, nil
}

// createTables creates the necessary tables for logs and balances
func (s *SQLiteLogStore) createTables(ctx context.Context) error {
	// Create logs table
	// Note: sequence is provided by the FSM, not auto-generated by the database
	_, err := s.db.ExecContext(ctx, `
		CREATE TABLE IF NOT EXISTS logs (
			id INTEGER,
			type TEXT NOT NULL,
			data TEXT NOT NULL,
			date TEXT,
			ledger TEXT NOT NULL,
			idempotency_key TEXT,
			idempotency_hash TEXT,
			sequence INTEGER NOT NULL DEFAULT 0,
			UNIQUE(ledger, idempotency_key),
			UNIQUE(ledger, idempotency_hash)
		);
		
		CREATE INDEX IF NOT EXISTS idx_logs_idempotency_key ON logs(idempotency_key);
		CREATE INDEX IF NOT EXISTS idx_logs_id ON logs(id);
		CREATE INDEX IF NOT EXISTS idx_logs_ledger ON logs(ledger);
		CREATE UNIQUE INDEX IF NOT EXISTS idx_logs_sequence ON logs(sequence);
	`)
	if err != nil {
		return fmt.Errorf("creating logs table: %w", err)
	}

	// Create balances table
	_, err = s.db.ExecContext(ctx, `
		CREATE TABLE IF NOT EXISTS balances (
			ledger TEXT NOT NULL,
			account TEXT NOT NULL,
			asset TEXT NOT NULL,
			balance TEXT NOT NULL DEFAULT '0',
			PRIMARY KEY (ledger, account, asset)
		);
		
		CREATE INDEX IF NOT EXISTS idx_balances_ledger ON balances(ledger);
		CREATE INDEX IF NOT EXISTS idx_balances_account ON balances(ledger, account);
	`)
	if err != nil {
		return fmt.Errorf("creating balances table: %w", err)
	}

	// Custom SQLite functions are registered globally before opening connections
	// No need to register them here

	// Create trigger to update balances on NEW_TRANSACTION log insert
	// For each posting: subtract from source, add to destination
	_, err = s.db.ExecContext(ctx, `
		CREATE TRIGGER IF NOT EXISTS update_balances
		AFTER INSERT ON logs
		WHEN NEW.type = 'NEW_TRANSACTION' OR NEW.type = 'REVERTED_TRANSACTION'
		BEGIN
			-- Update balances for source accounts (subtract amount)
			INSERT OR REPLACE INTO balances (ledger, account, asset, balance)
			SELECT 
				NEW.ledger,
				json_extract(posting.value, '$.source') AS account,
				json_extract(posting.value, '$.asset') AS asset,
				bigint_sub(
					COALESCE(
						(SELECT balance FROM balances WHERE ledger = NEW.ledger AND account = json_extract(posting.value, '$.source') AND asset = json_extract(posting.value, '$.asset')),
						'0'
					),
					CAST(json_extract(posting.value, '$.amount') AS TEXT)
				) AS balance
			FROM json_each(json_extract(NEW.data, '$.transaction.postings')) AS posting
			WHERE json_extract(posting.value, '$.source') IS NOT NULL
			  AND json_extract(posting.value, '$.source') != '';
			
			-- Update balances for destination accounts (add amount)
			INSERT OR REPLACE INTO balances (ledger, account, asset, balance)
			SELECT 
				NEW.ledger,
				json_extract(posting.value, '$.destination') AS account,
				json_extract(posting.value, '$.asset') AS asset,
				bigint_add(
					COALESCE(
						(SELECT balance FROM balances WHERE ledger = NEW.ledger AND account = json_extract(posting.value, '$.destination') AND asset = json_extract(posting.value, '$.asset')),
						'0'
					),
					CAST(json_extract(posting.value, '$.amount') AS TEXT)
				) AS balance
			FROM json_each(json_extract(NEW.data, '$.transaction.postings')) AS posting
			WHERE json_extract(posting.value, '$.destination') IS NOT NULL
			  AND json_extract(posting.value, '$.destination') != '';
		END;
	`)
	if err != nil {
		return fmt.Errorf("creating new transaction balances trigger: %w", err)
	}

	return nil
}

func init() {
	// Register bigint_add function
	err := sqlite.RegisterFunction("bigint_add", &sqlite.FunctionImpl{
		NArgs:         2,
		Deterministic: true,
		Scalar: func(ctx *sqlite.FunctionContext, args []driver.Value) (driver.Value, error) {
			if len(args) != 2 {
				return "0", nil
			}
			aStr, okA := args[0].(string)
			bStr, okB := args[1].(string)
			if !okA || !okB {
				return "0", nil
			}
			bigA, okA := new(big.Int).SetString(aStr, 10)
			bigB, okB := new(big.Int).SetString(bStr, 10)
			if !okA || !okB {
				return "0", nil
			}
			result := new(big.Int).Add(bigA, bigB)
			return result.String(), nil
		},
	})
	if err != nil {
		panic(err)
	}

	// Register bigint_sub function
	err = sqlite.RegisterFunction("bigint_sub", &sqlite.FunctionImpl{
		NArgs:         2,
		Deterministic: true,
		Scalar: func(ctx *sqlite.FunctionContext, args []driver.Value) (driver.Value, error) {
			if len(args) != 2 {
				return "0", nil
			}
			aStr, okA := args[0].(string)
			bStr, okB := args[1].(string)
			if !okA || !okB {
				return "0", nil
			}
			bigA, okA := new(big.Int).SetString(aStr, 10)
			bigB, okB := new(big.Int).SetString(bStr, 10)
			if !okA || !okB {
				return "0", nil
			}
			result := new(big.Int).Sub(bigA, bigB)
			return result.String(), nil
		},
	})
	if err != nil {
		panic(err)
	}
}

// Close closes the database connection
func (s *SQLiteLogStore) Close() error {
	return s.db.Close()
}

// InsertLogs inserts logs into the SQLite database (implements LogWriter)
func (s *SQLiteLogStore) InsertLogs(ctx context.Context, logs ...ledger.Log) error {
	if len(logs) == 0 {
		return nil
	}

	// Use a transaction for batch insert
	tx, err := s.db.BeginTx(ctx, nil)
	if err != nil {
		return fmt.Errorf("starting transaction: %w", err)
	}
	defer func() {
		_ = tx.Rollback()
	}()

	stmt, err := tx.PrepareContext(ctx, `
		INSERT INTO logs (type, data, date, ledger, idempotency_key, idempotency_hash, id, sequence)
		VALUES (?, ?, ?, ?, ?, ?, ?, ?)
		ON CONFLICT (sequence) DO NOTHING 
	`)
	if err != nil {
		return fmt.Errorf("preparing insert statement: %w", err)
	}
	defer func() {
		_ = stmt.Close()
	}()

	for _, log := range logs {
		// Marshal data to JSON
		dataJSON, err := json.Marshal(log.Data)
		if err != nil {
			return fmt.Errorf("marshaling log data: %w", err)
		}

		// Format date as RFC3339 string (SQLite stores dates as TEXT)
		var dateStr sql.NullString
		if !log.Date.IsZero() {
			dateStr = sql.NullString{
				String: log.Date.Format(stdtime.RFC3339),
				Valid:  true,
			}
		}

		var idempotencyKey sql.NullString
		if log.IdempotencyKey != "" {
			idempotencyKey = sql.NullString{
				String: log.IdempotencyKey,
				Valid:  true,
			}
		}

		var idempotencyHash sql.NullString
		if log.IdempotencyHash != "" {
			idempotencyHash = sql.NullString{
				String: log.IdempotencyHash,
				Valid:  true,
			}
		}

		var id sql.NullInt64
		if log.ID != nil {
			id = sql.NullInt64{
				Int64: int64(*log.ID),
				Valid: true,
			}
		}

		_, err = stmt.ExecContext(ctx,
			log.Type.String(),
			string(dataJSON),
			dateStr,
			log.Ledger,
			idempotencyKey,
			idempotencyHash,
			id,
			int64(log.Sequence),
		)
		if err != nil {
			return fmt.Errorf("inserting log: %w", err)
		}
	}

	if err := tx.Commit(); err != nil {
		return fmt.Errorf("committing transaction: %w", err)
	}

	s.logger.WithFields(map[string]any{"count": len(logs)}).Debugf("Logs inserted into SQLite")
	return nil
}

// GetLogWithIdempotencyKey retrieves a log by its idempotency key (implements LogReader)
func (s *SQLiteLogStore) GetLogWithIdempotencyKey(ctx context.Context, ledgerName string, idempotencyKey string) (*ledger.Log, error) {
	row := s.db.QueryRowContext(ctx, `
		SELECT id, type, data, date, ledger, idempotency_key, idempotency_hash, sequence
		FROM logs
		WHERE ledger = ? AND idempotency_key = ?
	`, ledgerName, idempotencyKey)

	log, err := s.scanLog(row)
	if err != nil {
		if err == sql.ErrNoRows {
			return nil, nil
		}
		return nil, fmt.Errorf("getting log by idempotency key: %w", err)
	}
	return log, nil
}

// GetLastLog retrieves the last log by ID for a specific ledger (implements LogReader)
func (s *SQLiteLogStore) GetLastLog(ctx context.Context, ledgerName string) (*ledger.Log, error) {
	row := s.db.QueryRowContext(ctx, `
		SELECT id, type, data, date, ledger, idempotency_key, idempotency_hash, sequence
		FROM logs
		WHERE ledger = ?
		ORDER BY id DESC
		LIMIT 1
	`, ledgerName)

	log, err := s.scanLog(row)
	if err != nil {
		if err == sql.ErrNoRows {
			return nil, nil
		}
		return nil, fmt.Errorf("getting last log: %w", err)
	}
	return log, nil
}

// scanLog scans a row into a Log struct
func (s *SQLiteLogStore) scanLog(row *sql.Row) (*ledger.Log, error) {
	var id sql.NullInt64
	var logType string
	var dataJSON string
	var dateStr sql.NullString
	var ledgerName string
	var idempotencyKey sql.NullString
	var idempotencyHash sql.NullString
	var sequence uint64

	err := row.Scan(&id, &logType, &dataJSON, &dateStr, &ledgerName, &idempotencyKey, &idempotencyHash, &sequence)
	if err != nil {
		return nil, err
	}

	log := &ledger.Log{}

	// Set ID
	if id.Valid {
		idVal := uint64(id.Int64)
		log.ID = &idVal
	}

	// Set sequence
	log.Sequence = sequence

	// Set type
	log.Type = ledger.LogTypeFromString(logType)

	// Unmarshal data using HydrateLog
	log.Data, err = ledger.HydrateLog(log.Type, []byte(dataJSON))
	if err != nil {
		return nil, fmt.Errorf("hydrating log data: %w", err)
	}

	// Set date
	if dateStr.Valid {
		date, err := stdtime.Parse(stdtime.RFC3339, dateStr.String)
		if err != nil {
			return nil, fmt.Errorf("parsing date: %w", err)
		}
		log.Date = libtime.New(date)
	}

	// Set ledger
	log.Ledger = ledgerName

	// Set idempotency fields
	if idempotencyKey.Valid {
		log.IdempotencyKey = idempotencyKey.String
	}
	if idempotencyHash.Valid {
		log.IdempotencyHash = idempotencyHash.String
	}

	return log, nil
}

// sqliteLogCursor implements Cursor[ledger.Log] for SQLite
type sqliteLogCursor struct {
	rows  *sql.Rows
	store *SQLiteLogStore
}

func (c *sqliteLogCursor) Next(ctx context.Context) (ledger.Log, error) {
	if !c.rows.Next() {
		if err := c.rows.Err(); err != nil {
			return ledger.Log{}, err
		}
		return ledger.Log{}, io.EOF
	}

	var id sql.NullInt64
	var logType string
	var dataJSON string
	var dateStr sql.NullString
	var ledgerName string
	var idempotencyKey sql.NullString
	var idempotencyHash sql.NullString
	var sequence uint64

	err := c.rows.Scan(&id, &logType, &dataJSON, &dateStr, &ledgerName, &idempotencyKey, &idempotencyHash, &sequence)
	if err != nil {
		return ledger.Log{}, fmt.Errorf("scanning log row: %w", err)
	}

	log := ledger.Log{}

	// Set ID
	if id.Valid {
		idVal := uint64(id.Int64)
		log.ID = &idVal
	}

	// Set sequence
	log.Sequence = sequence

	// Set type
	log.Type = ledger.LogTypeFromString(logType)

	// Unmarshal data using HydrateLog
	log.Data, err = ledger.HydrateLog(log.Type, []byte(dataJSON))
	if err != nil {
		return ledger.Log{}, fmt.Errorf("hydrating log data: %w", err)
	}

	// Set date
	if dateStr.Valid {
		date, err := stdtime.Parse(stdtime.RFC3339, dateStr.String)
		if err != nil {
			return ledger.Log{}, fmt.Errorf("parsing date: %w", err)
		}
		log.Date = libtime.New(date)
	}

	// Set ledger
	log.Ledger = ledgerName

	// Set idempotency fields
	if idempotencyKey.Valid {
		log.IdempotencyKey = idempotencyKey.String
	}
	if idempotencyHash.Valid {
		log.IdempotencyHash = idempotencyHash.String
	}

	return log, nil
}

func (c *sqliteLogCursor) Close() error {
	if c.rows != nil {
		return c.rows.Close()
	}
	return nil
}

// GetAllLogs returns a cursor to iterate over all logs (implements LogReader)
// Logs are returned in ascending order by sequence
// from: optional sequence number to start from (0 = from beginning)
func (s *SQLiteLogStore) GetAllLogs(ctx context.Context, from uint64, to uint64) (Cursor[ledger.Log], error) {
	query := `
		SELECT id, type, data, date, ledger, idempotency_key, idempotency_hash, sequence
		FROM logs
	`
	args := []interface{}{}
	whereClauses := []string{}
	if from > 0 {
		whereClauses = append(whereClauses, `sequence >= ?`)
		args = append(args, int64(from))
	}
	if to > 0 {
		whereClauses = append(whereClauses, `sequence <= ?`)
		args = append(args, int64(to))
	}
	if len(whereClauses) > 0 {
		query += ` WHERE ` + whereClauses[0]
		for i := 1; i < len(whereClauses); i++ {
			query += ` AND ` + whereClauses[i]
		}
	}
	query += ` ORDER BY sequence ASC`

	rows, err := s.db.QueryContext(ctx, query, args...)
	if err != nil {
		return nil, fmt.Errorf("querying logs: %w", err)
	}

	return &sqliteLogCursor{
		rows:  rows,
		store: s,
	}, nil
}

// GetLastSequenceID returns the highest sequence number in the logs table (implements LogWriter)
func (s *SQLiteLogStore) GetLastSequenceID(ctx context.Context) (uint64, error) {
	var maxSeq sql.NullInt64
	err := s.db.QueryRowContext(ctx, `SELECT MAX(sequence) FROM logs`).Scan(&maxSeq)
	if err != nil {
		return 0, fmt.Errorf("querying max sequence: %w", err)
	}
	if !maxSeq.Valid {
		// No logs yet, return 0
		return 0, nil
	}
	return uint64(maxSeq.Int64), nil
}

// GetBalances retrieves balances from the balances table (implements BalancesStore)
func (s *SQLiteLogStore) GetBalances(ctx context.Context, ledgerName string, balanceQuery map[string][]string) (ledger.Balances, error) {
	result := make(ledger.Balances)

	// If no query provided, return empty balances
	if len(balanceQuery) == 0 {
		return result, nil
	}

	// Build query for each account/asset combination
	for account, assets := range balanceQuery {
		if len(assets) == 0 {
			continue
		}

		// Initialize account map
		result[account] = make(map[string]*big.Int)

		// Build placeholders for IN clause
		placeholders := make([]string, len(assets))
		args := make([]interface{}, len(assets)+2)
		args[0] = ledgerName
		args[1] = account

		for i, asset := range assets {
			placeholders[i] = "?"
			args[i+2] = asset
		}

		query := fmt.Sprintf(`
			SELECT asset, balance FROM balances
			WHERE ledger = ? AND account = ? AND asset IN (%s)
		`, strings.Join(placeholders, ", "))

		rows, err := s.db.QueryContext(ctx, query, args...)
		if err != nil {
			return nil, fmt.Errorf("querying balances: %w", err)
		}
		defer rows.Close()

		for rows.Next() {
			var asset string
			var balanceStr string
			if err := rows.Scan(&asset, &balanceStr); err != nil {
				return nil, fmt.Errorf("scanning balance row: %w", err)
			}

			balance, ok := new(big.Int).SetString(balanceStr, 10)
			if !ok {
				return nil, fmt.Errorf("invalid balance string: %s", balanceStr)
			}
			result[account][asset] = balance
		}

		if err := rows.Err(); err != nil {
			return nil, fmt.Errorf("iterating balance rows: %w", err)
		}

		// Set zero balance for assets that don't exist in the database
		for _, asset := range assets {
			if _, exists := result[account][asset]; !exists {
				result[account][asset] = big.NewInt(0)
			}
		}
	}

	return result, nil
}

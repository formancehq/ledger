func TestJSONRecoverer_SanitizesPanic(t *testing.T) {
	t.Parallel()

	var logs bytes.Buffer
	logger := logging.NewDefaultLogger(&logs, false, false, false)

	panicking := http.HandlerFunc(func(http.ResponseWriter, *http.Request) {
		panic("secret invariant: /var/lib/ledger/pebble corrupted")
	})

	ctx := logging.ContextWithLogger(context.Background(), logger)
	ctx = context.WithValue(ctx, middleware.RequestIDKey, "corr-panic")

	w := httptest.NewRecorder()
	r := httptest.NewRequest(http.MethodGet, "/", nil).WithContext(ctx)

	jsonRecoverer(panicking).ServeHTTP(w, r)

	require.Equal(t, http.StatusInternalServerError, w.Code)
	require.Equal(t, "application/json", w.Header().Get("Content-Type"))

	resp := decodeResponse[ErrorResponse](t, w)
	require.Equal(t, "INTERNAL_ERROR", resp.ErrorCode)

	// The client must never see the raw panic value.
	require.Equal(t, "internal server error (correlation ID: corr-panic)", resp.ErrorMessage)
	require.NotContains(t, resp.ErrorMessage, "secret invariant")
	require.NotContains(t, resp.ErrorMessage, "pebble")

	// The raw panic value, the stack, and the correlation ID are logged
	// server-side so ops can resolve the client-reported ID.
	logged := logs.String()
	require.Contains(t, logged, "secret invariant: /var/lib/ledger/pebble corrupted")
	require.Contains(t, logged, "corr-panic")
	require.Contains(t, logged, "HTTP handler panicked")
}

func TestJSONRecoverer_PropagatesErrAbortHandler(t *testing.T) {
	t.Parallel()

	aborting := http.HandlerFunc(func(http.ResponseWriter, *http.Request) {
		panic(http.ErrAbortHandler)
	})

	w := httptest.NewRecorder()
	r := httptest.NewRequest(http.MethodGet, "/", nil)

	// ErrAbortHandler must be re-panicked so the server can handle it, not
	// swallowed into a JSON 500.
	require.PanicsWithValue(t, http.ErrAbortHandler, func() {
		jsonRecoverer(aborting).ServeHTTP(w, r)
	})
}

func TestJSONRecoverer_NoPanicPassesThrough(t *testing.T) {
	t.Parallel()

	ok := http.HandlerFunc(func(w http.ResponseWriter, _ *http.Request) {
		w.WriteHeader(http.StatusOK)
		_, _ = w.Write([]byte(`{"ok":true}`))
	})

	w := httptest.NewRecorder()
	r := httptest.NewRequest(http.MethodGet, "/", nil)

	jsonRecoverer(ok).ServeHTTP(w, r)

	require.Equal(t, http.StatusOK, w.Code)
	require.JSONEq(t, `{"ok":true}`, w.Body.String())
}

// TestNewHandler_SanitizesPanicEndToEnd drives a panicking route through the
// real NewHandler middleware chain (not jsonRecoverer in isolation) to lock in
// the two wiring properties the sanitization depends on: middleware.RequestID
// must run outer to jsonRecoverer (so the client-facing correlation ID matches
// the one logged server-side) and loggerMiddleware must run outer to it too (so
// logging.FromContext resolves the app logger inside recover(), not a bare
// stderr fallback). A reorder that breaks either property would keep the
// isolation tests green but fail this one.
func TestNewHandler_SanitizesPanicEndToEnd(t *testing.T) {
	t.Parallel()

	var logs bytes.Buffer
	logger := logging.NewDefaultLogger(&logs, false, false, false)

	// An Apply that returns no log is a backend contract violation:
	// handleCreateLedger panics, which the real chain must turn into a
	// sanitized JSON 500 (see TestHandleCreateLedger_NoLogReturned).
	backend := NewMockBackend(gomock.NewController(t))
	backend.EXPECT().Apply(gomock.Any(), gomock.Any()).DoAndReturn(
		func(_ context.Context, _ *servicepb.ApplyRequest) ([]*commonpb.Log, error) {
			return []*commonpb.Log{}, nil
		}).AnyTimes()

	handler := NewHandler(logger, backend, internalauth.AuthConfig{}, version.Info{})

	w := httptest.NewRecorder()
	r := httptest.NewRequest(http.MethodPost, APIVersionPrefix+"/test-ledger", nil)

	handler.ServeHTTP(w, r)

	require.Equal(t, http.StatusInternalServerError, w.Code)
	require.Equal(t, "application/json", w.Header().Get("Content-Type"))

	resp := decodeResponse[ErrorResponse](t, w)
	require.Equal(t, "INTERNAL_ERROR", resp.ErrorCode)

	// The client body is the generic sanitized message carrying a correlation
	// ID generated by middleware.RequestID — proof RequestID ran outer to the
	// recoverer.
	const prefix = "internal server error (correlation ID: "
	require.True(t, strings.HasPrefix(resp.ErrorMessage, prefix),
		"unexpected body: %q", resp.ErrorMessage)
	id := strings.TrimSuffix(strings.TrimPrefix(resp.ErrorMessage, prefix), ")")
	require.NotEmpty(t, id)

	// The same ID (and the panic marker) must be logged through the injected
	// app logger — proof loggerMiddleware ran outer to the recoverer, so
	// logging.FromContext did not degrade to a bare stderr logger.
	logged := logs.String()
	require.Contains(t, logged, "HTTP handler panicked")
	require.Contains(t, logged, id)
}
